#pragma once

struct Tile {
    uint8_t type;
    Vector2 pos;
};

struct Map {
    uint8_t tiles[300];
    Vector2 playerSpawn;
    std::string mapID;
    std::string nextMapID;
};

void makeTilesFromMap(Map map, std::vector<Tile> *tiles)
{
    tiles->clear();

    for(int i = 0; i < 15; i++)
    {
        for(int j = 0; j < 20; j++)
        {
            Tile t;
            t.pos.x = j * 40;
            t.pos.y = i * 40;
            t.type = map.tiles[j + (i * 20)];
            tiles->push_back(t);
        }   
    }
}

void renderTiles(std::vector<Tile> tiles, std::vector<Sprite> sprites)
{
    for(Tile tile : tiles)
    {
        std::string id;
        switch (tile.type)
        {
            case 0: id = ""; break;
            case 1: id = "tile_basic"; break;
            case 2: id = "door"; break;
            case 3: id = "spike"; break;
            default: id = ""; break;
        }

        DrawSpriteFromVector(id, tile.pos, {40, 40}, sprites);
    }
}

bool checkBoxCollison(Vector2 posA, Vector2 sizeA, Vector2 posB, Vector2 sizeB)
{
    if(posA.x < posB.x + sizeB.x &&
        posA.x + sizeA.x > posB.x &&
        posA.y < posB.y + sizeB.y &&
        posA.y + sizeA.y > posB.y
    ) { return true; }
    else { return false; }
}

void LoadMap(std::string mapID, std::vector<Map>& maps, std::vector<Tile> *tiles, Map &loadedMap)
{
    std::cout << "GAME: Loading map " << mapID << "\n";
    for(Map m : maps)
    {
        if(m.mapID == mapID)
        {
            makeTilesFromMap(m, tiles);
            loadedMap = m;
        }
    }
}

void LoadMapToVector(Map map, std::vector<Map>& maps)
{
    maps.push_back(map);
}

